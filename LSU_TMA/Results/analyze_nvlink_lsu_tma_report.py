#!/usr/bin/env python3
"""Analyze NVLink RX/TX utilization during LSU/TMA persistent kernels.

The script accepts Nsight Systems .sqlite files, or .nsys-rep files that can be
exported to sibling .sqlite files with `nsys export`. It targets reports
generated by:

  - nvlink_lsu_tma_copy_test.py
  - nvlink_lsu_tma_round_robin_copy_test.py

By default it analyzes the collected GPU at bus ID 0000:03:00.0, skips one
warmup kernel launch, and averages NVLink RX/TX metric samples over the
remaining persistent kernel window.
"""

from __future__ import annotations

import argparse
import csv
import json
import shutil
import sqlite3
import subprocess
import sys
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path
from typing import Any


SCRIPT_DIR = Path(__file__).resolve().parent
DEFAULT_GPU_BUS_ID = "0000:03:00.0"
DEFAULT_SKIP_WARMUP_KERNELS = 1

KERNEL_NAME_SUBSTRINGS = (
    "lsu_copy_kernel",
    "tma_copy_kernel",
    "lsu_round_robin_copy_kernel",
    "tma_round_robin_copy_kernel",
)


@dataclass(frozen=True)
class KernelEvent:
    start_ns: int
    end_ns: int
    device_id: int
    name: str
    short_name: str | None
    grid: tuple[int, int, int]
    block: tuple[int, int, int]
    dynamic_shared_memory: int

    @property
    def duration_ns(self) -> int:
        return self.end_ns - self.start_ns


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Compute average NVLink RX/TX utilization during LSU/TMA persistent "
            "kernel execution."
        )
    )
    parser.add_argument(
        "reports",
        nargs="*",
        type=Path,
        default=default_reports(),
        help=(
            "Input .sqlite or .nsys-rep files. Defaults to discovered .sqlite "
            "files below this directory."
        ),
    )
    parser.add_argument(
        "--gpu-bus-id",
        default=DEFAULT_GPU_BUS_ID,
        help=f"PCI bus ID of the collected GPU. Default: {DEFAULT_GPU_BUS_ID}",
    )
    parser.add_argument(
        "--kernel-name",
        default=None,
        help=(
            "Kernel name substring to analyze. Defaults to auto-detecting one "
            "of: " + ", ".join(KERNEL_NAME_SUBSTRINGS)
        ),
    )
    parser.add_argument(
        "--skip-warmup-kernels",
        type=int,
        default=DEFAULT_SKIP_WARMUP_KERNELS,
        help=(
            "Ignore this many earliest matching kernels on the collected GPU. "
            f"Default: {DEFAULT_SKIP_WARMUP_KERNELS}."
        ),
    )
    parser.add_argument(
        "--force-export",
        action="store_true",
        help="For .nsys-rep input, regenerate the sibling .sqlite with nsys export.",
    )
    parser.add_argument(
        "--nvlink-peak-gb-s",
        type=float,
        default=None,
        help=(
            "Optional peak unidirectional NVLink bandwidth in decimal GB/s. "
            "When provided, metric percentages are also converted to GB/s."
        ),
    )
    parser.add_argument(
        "--json",
        action="store_true",
        help="Print machine-readable JSON.",
    )
    parser.add_argument(
        "--csv",
        action="store_true",
        help="Print one CSV row per report.",
    )
    return parser.parse_args()


def default_reports() -> list[Path]:
    sqlites = sorted(SCRIPT_DIR.glob("**/*.sqlite"))
    if sqlites:
        return sqlites

    reports: list[Path] = []
    for nsys_report in sorted(SCRIPT_DIR.glob("**/*.nsys-rep")):
        if not nsys_report.with_suffix(".sqlite").exists():
            reports.append(nsys_report)
    return reports


def resolve_sqlite(report: Path, force_export: bool) -> Path:
    report = report.expanduser().resolve()
    if report.suffix == ".sqlite":
        if not report.exists():
            raise FileNotFoundError(report)
        return report

    if report.suffix != ".nsys-rep":
        raise ValueError(f"Unsupported input suffix: {report.suffix}")

    sqlite_path = report.with_suffix(".sqlite")
    if sqlite_path.exists() and not force_export:
        return sqlite_path

    nsys = shutil.which("nsys")
    if nsys is None:
        if sqlite_path.exists():
            return sqlite_path
        raise RuntimeError(
            f"{sqlite_path} does not exist and `nsys` was not found to export {report}"
        )

    cmd = [
        nsys,
        "export",
        "--type",
        "sqlite",
        "--force-overwrite=true",
        "--output",
        str(sqlite_path),
        str(report),
    ]
    subprocess.run(cmd, check=True)
    return sqlite_path


def connect(sqlite_path: Path) -> sqlite3.Connection:
    conn = sqlite3.connect(sqlite_path)
    conn.row_factory = sqlite3.Row
    return conn


def scalar(conn: sqlite3.Connection, sql: str, params: tuple[Any, ...] = ()) -> Any:
    row = conn.execute(sql, params).fetchone()
    if row is None:
        return None
    return row[0]


def table_exists(conn: sqlite3.Connection, table_name: str) -> bool:
    return bool(
        scalar(
            conn,
            "SELECT 1 FROM sqlite_master WHERE type = 'table' AND name = ?",
            (table_name,),
        )
    )


def find_gpu(conn: sqlite3.Connection, bus_id: str) -> sqlite3.Row:
    row = conn.execute(
        """
        SELECT id, cuDevice, name, busLocation, uuid
        FROM TARGET_INFO_GPU
        WHERE lower(busLocation) = lower(?)
        """,
        (bus_id,),
    ).fetchone()
    if row is None:
        known = [
            dict(r)
            for r in conn.execute(
                "SELECT cuDevice, name, busLocation FROM TARGET_INFO_GPU ORDER BY cuDevice"
            )
        ]
        raise RuntimeError(f"GPU bus ID {bus_id!r} not found. Known GPUs: {known}")
    return row


def observed_kernel_names(conn: sqlite3.Connection) -> list[str]:
    if not table_exists(conn, "CUPTI_ACTIVITY_KIND_KERNEL"):
        return []
    rows = conn.execute(
        """
        SELECT DISTINCT s.value AS name
        FROM CUPTI_ACTIVITY_KIND_KERNEL AS k
        JOIN StringIds AS s
            ON s.id = k.demangledName
        ORDER BY s.value
        """
    ).fetchall()
    return [str(row["name"]) for row in rows]


def detect_kernel_name(conn: sqlite3.Connection, requested: str | None) -> str:
    if requested:
        return requested

    names = observed_kernel_names(conn)
    matches: list[str] = []
    for candidate in KERNEL_NAME_SUBSTRINGS:
        if any(candidate in name for name in names):
            matches.append(candidate)

    if not matches:
        likely_names = [name for name in names if "kernel" in name.lower()]
        raise RuntimeError(
            "Could not auto-detect an LSU/TMA copy kernel. "
            f"Kernel-like names in report: {likely_names[:30]}"
        )
    if len(matches) > 1:
        raise RuntimeError(
            "Multiple LSU/TMA copy kernels were found; pass --kernel-name. "
            f"Matches: {matches}"
        )
    return matches[0]


def load_kernel_events(
    conn: sqlite3.Connection, device_id: int, kernel_name: str
) -> list[KernelEvent]:
    if not table_exists(conn, "CUPTI_ACTIVITY_KIND_KERNEL"):
        raise RuntimeError("CUPTI_ACTIVITY_KIND_KERNEL table is missing")

    rows = conn.execute(
        """
        SELECT
            k.start,
            k.end,
            k.deviceId,
            demangled.value AS kernelName,
            short.value AS shortName,
            k.gridX,
            k.gridY,
            k.gridZ,
            k.blockX,
            k.blockY,
            k.blockZ,
            k.dynamicSharedMemory
        FROM CUPTI_ACTIVITY_KIND_KERNEL AS k
        JOIN StringIds AS demangled
            ON demangled.id = k.demangledName
        LEFT JOIN StringIds AS short
            ON short.id = k.shortName
        WHERE k.deviceId = ?
            AND demangled.value LIKE ?
        ORDER BY k.start, k.end
        """,
        (device_id, f"%{kernel_name}%"),
    ).fetchall()

    events: list[KernelEvent] = []
    for row in rows:
        if row["end"] <= row["start"]:
            continue
        events.append(
            KernelEvent(
                start_ns=int(row["start"]),
                end_ns=int(row["end"]),
                device_id=int(row["deviceId"]),
                name=str(row["kernelName"]),
                short_name=row["shortName"],
                grid=(int(row["gridX"]), int(row["gridY"]), int(row["gridZ"])),
                block=(int(row["blockX"]), int(row["blockY"]), int(row["blockZ"])),
                dynamic_shared_memory=int(row["dynamicSharedMemory"]),
            )
        )
    return events


def average(values: list[float]) -> float | None:
    if not values:
        return None
    return sum(values) / len(values)


def summarize_durations(events: list[KernelEvent]) -> dict[str, float | int | None]:
    durations_ns = [event.duration_ns for event in events]
    return {
        "count": len(durations_ns),
        "average_ns": average([float(v) for v in durations_ns]),
        "average_us": (
            average([float(v) for v in durations_ns]) / 1_000.0
            if durations_ns
            else None
        ),
        "min_ns": min(durations_ns) if durations_ns else None,
        "max_ns": max(durations_ns) if durations_ns else None,
    }


def summarize_nvlink_metrics(
    conn: sqlite3.Connection,
    first_start_ns: int | None,
    last_end_ns: int | None,
    peak_gb_s: float | None,
    include_protocol: bool,
) -> dict[str, dict[str, Any]]:
    if first_start_ns is None or last_end_ns is None:
        return {}
    if not table_exists(conn, "GPU_METRICS"):
        raise RuntimeError("GPU_METRICS table is missing")

    user_filter = "" if include_protocol else "AND ti.metricName LIKE '%User Data%'"
    rows = conn.execute(
        f"""
        SELECT
            gm.timestamp,
            CASE
                WHEN ti.metricName LIKE 'NVLink RX%' THEN 'rx'
                WHEN ti.metricName LIKE 'NVLink TX%' THEN 'tx'
            END AS direction,
            SUM(gm.value) AS value,
            GROUP_CONCAT(DISTINCT ti.metricName) AS metricNames
        FROM GPU_METRICS AS gm
        JOIN TARGET_INFO_GPU_METRICS AS ti
            ON ti.typeId = gm.typeId
            AND ti.metricId = gm.metricId
        WHERE gm.timestamp BETWEEN ? AND ?
            AND (ti.metricName LIKE 'NVLink RX%' OR ti.metricName LIKE 'NVLink TX%')
            {user_filter}
        GROUP BY gm.timestamp, direction
        HAVING direction IS NOT NULL
        ORDER BY gm.timestamp, direction
        """,
        (first_start_ns, last_end_ns),
    ).fetchall()

    values: dict[str, list[float]] = defaultdict(list)
    metric_names: dict[str, set[str]] = defaultdict(set)
    for row in rows:
        direction = row["direction"]
        values[direction].append(float(row["value"]))
        for name in (row["metricNames"] or "").split(","):
            if name:
                metric_names[direction].add(name)

    result: dict[str, dict[str, Any]] = {}
    for direction in ("rx", "tx"):
        direction_values = values.get(direction, [])
        avg_percent = average(direction_values)
        entry: dict[str, Any] = {
            "sample_count": len(direction_values),
            "average_percent_of_peak": avg_percent,
            "min_percent_of_peak": min(direction_values) if direction_values else None,
            "max_percent_of_peak": max(direction_values) if direction_values else None,
            "included_metrics": sorted(metric_names.get(direction, set())),
        }
        if avg_percent is not None and peak_gb_s is not None:
            entry["average_gb_s"] = avg_percent / 100.0 * peak_gb_s
        result[direction] = entry
    return result


def build_summary(
    conn: sqlite3.Connection,
    sqlite_path: Path,
    bus_id: str,
    kernel_name: str | None,
    skip_warmup_kernels: int,
    peak_gb_s: float | None,
) -> dict[str, Any]:
    if skip_warmup_kernels < 0:
        raise ValueError("--skip-warmup-kernels must be non-negative")

    gpu = find_gpu(conn, bus_id)
    detected_kernel_name = detect_kernel_name(conn, kernel_name)
    all_events = load_kernel_events(conn, int(gpu["cuDevice"]), detected_kernel_name)
    events = all_events[skip_warmup_kernels:]

    if not all_events:
        raise RuntimeError(
            f"No kernels matching {detected_kernel_name!r} were found on "
            f"CUDA device {gpu['cuDevice']} ({gpu['busLocation']})"
        )
    if not events:
        raise RuntimeError(
            f"All {len(all_events)} matching kernels were skipped by "
            f"--skip-warmup-kernels={skip_warmup_kernels}"
        )

    first_start_ns = min(event.start_ns for event in events)
    last_end_ns = max(event.end_ns for event in events)
    first_event = events[0]

    return {
        "sqlite_path": str(sqlite_path),
        "label": sqlite_path.stem,
        "gpu": {
            "bus_id": gpu["busLocation"],
            "name": gpu["name"],
            "target_info_id": gpu["id"],
            "cuda_device_id": gpu["cuDevice"],
            "uuid": gpu["uuid"],
        },
        "kernel": {
            "requested_name": kernel_name,
            "matched_name_substring": detected_kernel_name,
            "example_demangled_name": first_event.name,
            "example_short_name": first_event.short_name,
            "grid": first_event.grid,
            "block": first_event.block,
            "dynamic_shared_memory": first_event.dynamic_shared_memory,
        },
        "counts": {
            "matching_kernel_count_before_warmup_skip": len(all_events),
            "skipped_warmup_kernel_count": min(skip_warmup_kernels, len(all_events)),
            "selected_kernel_count": len(events),
        },
        "kernel_window": {
            "first_kernel_start_ns": first_start_ns,
            "last_kernel_end_ns": last_end_ns,
            "duration_ns": last_end_ns - first_start_ns,
            "duration_us": (last_end_ns - first_start_ns) / 1_000.0,
        },
        "selected_kernel_durations": summarize_durations(events),
        "nvlink_user_data_metrics_over_kernel_window": summarize_nvlink_metrics(
            conn, first_start_ns, last_end_ns, peak_gb_s, include_protocol=False
        ),
        "nvlink_user_plus_protocol_metrics_over_kernel_window": summarize_nvlink_metrics(
            conn, first_start_ns, last_end_ns, peak_gb_s, include_protocol=True
        ),
    }


def fmt_float(value: float | None, digits: int = 3) -> str:
    if value is None:
        return "n/a"
    return f"{value:.{digits}f}"


def fmt_ns_as_s_plus_ms(value_ns: int | None) -> str:
    if value_ns is None:
        return "n/a"
    seconds = value_ns // 1_000_000_000
    remainder_ms = (value_ns % 1_000_000_000) / 1_000_000.0
    return f"{seconds}s + {remainder_ms:.3f}ms"


def csv_row(summary: dict[str, Any]) -> dict[str, Any]:
    user = summary["nvlink_user_data_metrics_over_kernel_window"]
    all_data = summary["nvlink_user_plus_protocol_metrics_over_kernel_window"]
    return {
        "label": summary["label"],
        "sqlite_path": summary["sqlite_path"],
        "gpu_bus_id": summary["gpu"]["bus_id"],
        "cuda_device_id": summary["gpu"]["cuda_device_id"],
        "kernel": summary["kernel"]["matched_name_substring"],
        "selected_kernel_count": summary["counts"]["selected_kernel_count"],
        "kernel_window_us": summary["kernel_window"]["duration_us"],
        "user_rx_percent": user.get("rx", {}).get("average_percent_of_peak"),
        "user_tx_percent": user.get("tx", {}).get("average_percent_of_peak"),
        "user_rx_samples": user.get("rx", {}).get("sample_count", 0),
        "user_tx_samples": user.get("tx", {}).get("sample_count", 0),
        "all_rx_percent": all_data.get("rx", {}).get("average_percent_of_peak"),
        "all_tx_percent": all_data.get("tx", {}).get("average_percent_of_peak"),
        "all_rx_samples": all_data.get("rx", {}).get("sample_count", 0),
        "all_tx_samples": all_data.get("tx", {}).get("sample_count", 0),
    }


def print_csv(summaries: list[dict[str, Any]]) -> None:
    rows = [csv_row(summary) for summary in summaries]
    if not rows:
        return
    writer = csv.DictWriter(sys.stdout, fieldnames=list(rows[0].keys()))
    writer.writeheader()
    writer.writerows(rows)


def print_table(summaries: list[dict[str, Any]]) -> None:
    print(
        "label,kernel,window_us,user_rx_percent,user_tx_percent,"
        "all_rx_percent,all_tx_percent,samples_rx,samples_tx"
    )
    for summary in summaries:
        row = csv_row(summary)
        print(
            f"{row['label']},"
            f"{row['kernel']},"
            f"{fmt_float(row['kernel_window_us'])},"
            f"{fmt_float(row['user_rx_percent'])},"
            f"{fmt_float(row['user_tx_percent'])},"
            f"{fmt_float(row['all_rx_percent'])},"
            f"{fmt_float(row['all_tx_percent'])},"
            f"{row['user_rx_samples']},"
            f"{row['user_tx_samples']}"
        )


def print_text(summary: dict[str, Any]) -> None:
    gpu = summary["gpu"]
    kernel = summary["kernel"]
    counts = summary["counts"]
    window = summary["kernel_window"]
    duration = summary["selected_kernel_durations"]
    nvlink_user = summary["nvlink_user_data_metrics_over_kernel_window"]
    nvlink_all = summary["nvlink_user_plus_protocol_metrics_over_kernel_window"]

    print(f"SQLite input: {summary['sqlite_path']}")
    print(
        "GPU: "
        f"{gpu['bus_id']} ({gpu['name']}, CUDA device {gpu['cuda_device_id']})"
    )
    print(f"Kernel match: {kernel['matched_name_substring']}")
    print(f"Example kernel: {kernel['example_demangled_name']}")
    print(f"Grid: {kernel['grid']}, block: {kernel['block']}")
    print(
        "Warmup skip: "
        f"{counts['skipped_warmup_kernel_count']} of "
        f"{counts['matching_kernel_count_before_warmup_skip']} matching kernels"
    )
    print()
    print("1. Persistent kernel window")
    print(
        "   "
        f"{fmt_ns_as_s_plus_ms(window['first_kernel_start_ns'])} to "
        f"{fmt_ns_as_s_plus_ms(window['last_kernel_end_ns'])} "
        f"({fmt_float(window['duration_us'])} us)"
    )
    print(
        "   "
        f"Selected kernels: {counts['selected_kernel_count']}, "
        f"average kernel duration: {fmt_float(duration['average_us'])} us"
    )
    print()
    print("2. Average NVLink RX/TX metrics over the persistent kernel window")
    for label, data in (
        ("user data", nvlink_user),
        ("user + protocol data", nvlink_all),
    ):
        rx = data.get("rx", {})
        tx = data.get("tx", {})
        print(
            f"   {label}: "
            f"RX {fmt_float(rx.get('average_percent_of_peak'))}% of peak, "
            f"TX {fmt_float(tx.get('average_percent_of_peak'))}% of peak "
            f"(samples RX={rx.get('sample_count', 0)}, TX={tx.get('sample_count', 0)})"
        )
        if "average_gb_s" in rx or "average_gb_s" in tx:
            print(
                "      converted: "
                f"RX {fmt_float(rx.get('average_gb_s'))} GB/s, "
                f"TX {fmt_float(tx.get('average_gb_s'))} GB/s"
            )


def analyze_report(report: Path, args: argparse.Namespace) -> dict[str, Any]:
    sqlite_path = resolve_sqlite(report, args.force_export)
    with connect(sqlite_path) as conn:
        return build_summary(
            conn,
            sqlite_path=sqlite_path,
            bus_id=args.gpu_bus_id,
            kernel_name=args.kernel_name,
            skip_warmup_kernels=args.skip_warmup_kernels,
            peak_gb_s=args.nvlink_peak_gb_s,
        )


def main() -> int:
    args = parse_args()
    if not args.reports:
        print("error: no input reports found", file=sys.stderr)
        return 1

    try:
        summaries = [analyze_report(report, args) for report in args.reports]
    except (
        OSError,
        RuntimeError,
        sqlite3.Error,
        subprocess.CalledProcessError,
        ValueError,
    ) as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1

    if args.json:
        payload: Any = summaries[0] if len(summaries) == 1 else summaries
        print(json.dumps(payload, indent=2, sort_keys=True))
    elif args.csv:
        print_csv(summaries)
    elif len(summaries) == 1:
        print_text(summaries[0])
    else:
        print_table(summaries)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
